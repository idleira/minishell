#include "libft.h"

char	*ft_strdup(const char *str)
{
	int		i;
	char	*dup;
	int		strlen;

	i = 0;
	strlen = ft_strlen (str);
	dup = malloc(sizeof(char) * (strlen + 1));
	if (!dup)
		return (NULL);
	while (str[i] != '\0')
	{
		dup[i] = str[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}